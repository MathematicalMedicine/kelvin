/*
  Use DCDFLIB's cumulative distribution function for Student's T distribution.
*/

double t_cdf (double x, double degFree) {
  double P, Q, bound;
  int type = 1, status;

#ifdef USE_GSL
  return(gsl_cdf_tdist_P (x, degFree));
#endif

  cdft(&type, &P, &Q, &x, &degFree, &status, &bound);
  if (status !=0 ) {
    printf ("In t_cdf, cdft error w/status %d, bound %g\n", status, bound);
    exit (EXIT_FAILURE);
  }

  //  fprintf (stderr, "t_cdf gives %g for x of %g\n", P, x);

  //  fprintf (stderr, "t_cdf gives %g at %gdf vs GSL of %g (difference is %g) for x of %g\n", result,
  //  	   degFree, gsl_cdf_tdist_P (x, degFree), (result - gsl_cdf_tdist_P (x, degFree), x);

  return (P);
}

/*
  Use DCDFLIB's ln(gamma()) function to compute the probability density function for
  chi square distribution.
*/
double chisq_pdf (double x, double degFree) {
  double result;

#ifdef USE_GSL
  return(gsl_ran_chisq_pdf(x, degFree));
#endif

  if (x <= 0)
    return ((double) 0.0);

  double halfDF = degFree/2;
  result = exp(degFree/2*log(.5)+(degFree/2-1)*log(x)-x/2-alngam(&halfDF));

  return(result);
}

/*
  Use DCDFLIB's cumulative distribution function for chi square distribution.
*/

double chisq_cdf (double x, double degFree) {
  double P, Q, bound;
  int type = 1, status;

#ifdef USE_GSL
  return(gsl_cdf_chisq_P(x, degFree));
#endif

  cdfchi(&type, &P, &Q, &x, &degFree, &status, &bound);
  if (status !=0 ) {
    printf ("In t_cdf, cdft error w/status %d, bound %g\n", status, bound);
    exit (EXIT_FAILURE);
  }

  return (P);
}

