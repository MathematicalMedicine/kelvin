double interpolate (double xTarget, int tableSize, double *tableX, double *tableY) {

  //  fprintf (stdout, "xTarget is %g, tableSize is %d, tableX[0] is %g, tableX[tableSize-1] is %g\n",
  //	   xTarget, tableSize, tableX[0], tableX[tableSize-1]);

  // Handle boundary cases...
  if (xTarget <= tableX[0])
    return (tableY[0]);
  if (xTarget >= tableX[tableSize-1])
    return (tableY[tableSize-1]);

  // Simple binary search for largest tableX <= xTarget

  int i, lo = 0, hi = tableSize;
  do {
    i = ((hi - lo) / 2 + lo);
    if (xTarget < tableX[i])
      hi = i - 1;
    else
      if (xTarget > tableX[i+1])
	lo = i + 1;
      else {
	//	fprintf (stdout, "x is %d between %.15g and %.15g giving y between %.15g and %.15g\n",
	//		 i, tableX[i], tableX[i+1], tableY[i], tableY[i+1]);
	return (tableY[i] + ((tableY[i+1]-tableY[i]) * ((xTarget-tableX[i]) / (tableX[i+1]-tableX[i]))));
      }
  } while (1);
}

// Just calculate the probability density function given mean and stdDev
double gaussian_pdf (double x, double mean, double stdDev) {
  //  fprintf (stderr, "gaussian_pdf\n");
#ifdef USE_GSL
  return (gsl_ran_gaussian_pdf (x, stdDev));
#else
  return (1/(stdDev*sqrt(2.0*M_PI)))*exp(-((x-mean)*(x-mean)/(2*stdDev*stdDev)));
#endif
}

double ugaussian_pdf (double x) {
  return gaussian_pdf (x, (double) 0.0, (double) 1.0);
}

